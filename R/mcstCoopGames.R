#' Cost Allocations based on the Cycle-complete Game for MCSTP
#'
#' @description
#' This function computes the cost allocation for the cycle-complete game
#' (Trudeau, 2012) associated with a minimum cost spanning tree problem \eqn{(N_0, C)}, using a specified solution concept. The
#' cycle-complete game, denoted as \eqn{(N, v_C^c)} \eqn{(}or simply \eqn{(N, v^c)}\eqn{)}, is
#' defined as the private game associated with the cycle-complete network
#' \eqn{(N_0, C^*)}.
#'
#' @details
#' The cycle-complete game \eqn{(N, v_C^c)} is a cooperative game with
#' transferable utility (TU game) defined for each coalition \eqn{S \subseteq N} as:
#'
#' \deqn{v_C^c(S) = m(S_0, C^*),}
#'
#' where \eqn{m(S_0, C^*)} is the cost of the minimum spanning tree of the
#' subproblem induced by the agents in \eqn{S} and the source, evaluated over
#' the cycle-complete costs \eqn{C^*}. The cycle-complete cost is defined as
#' the minimum between the direct cost \eqn{c_{ij}} and the smallest value of
#' the maximum arc cost over all cycles containing \eqn{i} and \eqn{j}, i.e.,
#' \eqn{c_{ij}^* = \min(c_{ij}, \min_{f \in \mathcal{C}(i,j)} \max_{e \in f} c_e),}
#' where \eqn{\mathcal{C}(i,j)} denotes the set of all cycles that include both \eqn{i} and \eqn{j}.
#'
#' For computational efficiency, \eqn{C^*} is obtained via
#' the irreducible matrices \eqn{\bar{C}} of the subgraphs as:
#'
#' \deqn{c_{ij}^* = \max_{k \in N \setminus \{i,j\}} {\bar{c}}_{ij}^{N \setminus \{k\}} \text{ for } i,j \in N,}
#'
#' and
#'
#' \deqn{c_{0i}^* = \max_{k \in N \setminus \{i\}} {\bar{c}}_{0i}^{N \setminus \{k\}} \text{ for } i \in N,}
#'
#' where \eqn{{\bar{C}}^{N \setminus \{k\}}} is the irreducible cost matrix of
#' the network excluding agent \eqn{k}. If no agents can be removed, the
#' irreducible cost of the full network is used.
#'
#' Under \code{method = "exact"}, the function builds the full characteristic
#' function (all \eqn{2^n - 1} values). Depending on the chosen solution concept,
#' it computes the allocation via \code{\link[CoopGame]{shapleyValue}} (called
#' \emph{cycle-complete solution}, which relies on the coalition-based formula)
#' or \code{\link[CoopGame]{nucleolus}}.
#'
#' For larger networks, \code{method = "montecarlo"} provides an approximation
#' of the Shapley value through random sampling using \eqn{n_{sim}} permutations. This approach
#' implements lazy evaluation based on the permutation formula of the Shapley value:
#' instead of computing the MST cost for all \eqn{2^n - 1}
#' possible subsets, it only evaluates the specific subcoalitions generated by
#' the random permutations.
#'
#' This is highly useful when the number of agents is large,
#' as it significantly reduces memory usage and computation time while providing a
#' very accurate approximation of the \emph{cycle-complete solution}. For networks with few players, the
#' exact method is preferred to avoid potential overestimations.
#'
#' For more details on these formulas, see \code{\link{mcstRules}}.
#'
#' @inheritParams mcstGamePrivate
#'
#' @inheritSection mcstGamePrivate Supported formats for \code{C}
#'
#' @note
#' The cycle-complete game is concave, which implies that its Shapley value
#' (the \emph{cycle-complete solution}) belongs to its core. Moreover,
#' \eqn{\mathrm{core}(N,v^c) \subseteq \mathrm{core}(N,v^p)}.
#'
#' @return A list containing:
#' \itemize{
#'    \item \code{coalitions}: a character vector representing all possible coalitions \eqn{S \subseteq N}, ordered by cardinality. If \code{method = "montecarlo"}, returns a string indicating that lazy evaluation was used.
#'    \item \code{C_cc}: the cycle-complete cost matrix \eqn{C^*}.
#'    \item \code{v_c}: a numeric vector containing the characteristic function values \eqn{v^c(S)} for all coalitions. If \code{method = "montecarlo"}, returns a string indicating that lazy evaluation was used.
#'    \item \code{contributions}: a data frame with the marginal contributions for the sampled permutations. If \code{method = "exact"}, returns a string indicating that this table is not used.
#'    \item \code{allocation}: a numeric vector containing the computed cost allocation for each agent.
#'    \item \code{total}: the total cost of the MCST, \eqn{v^c(N)}.
#'    \item \code{nperms}: the number of permutations \code{nsim} used in the calculation. If \code{method = "exact"}, returns a string indicating that it was not computed.
#'    \item \code{percentage}: the share of the total cost allocated to each agent.
#'    \item \code{ranking}: ranking of agents by cost (from highest to lowest; ties marked with *).
#'    \item \code{method}: the method used for the calculation (\code{"exact"} or \code{"montecarlo"}).
#'    \item \code{sol}: the solution concept used (\code{"shapley"} or \code{"nucleolus"}).
#' }
#'
#' @seealso
#' \code{\link{mcstGamePrivate}} for the pessimistic game based on the original costs.
#'
#' \code{\link{mcstGameIrred}} for the pessimistic game based on the irreducible cost matrix \eqn{\bar{C}}.
#'
#' \code{\link{mcstRules}} for an overview of the available rules and analysis tools in the package.
#'
#' @references
#' Bergantiños G, Vidal-Puga J (2021) A review of cooperative rules
#' and their associated algorithms for minimum-cost spanning tree problems.
#' SERIEs, 12:73-100
#'
#' Trudeau C (2012) A new stable and more responsible cost sharing solution
#' for mcst problems. Games Econom Behav 75(1):402–412
#'
#' @examples
#' # Simple vector input
#' mcstGameCC(c(12, 15, 20, 4, 6, 8), sol = "shapley", draw = TRUE)
#'
#' # Input with infinite costs (disconnected nodes)
#' C_inf <- c(5, 9, Inf, 15, 6, Inf, 7, Inf, Inf, Inf,
#'            Inf, 8, 7, Inf, Inf, 5, Inf, Inf, 8, 9, 11)
#' mcstGameCC(C_inf, sol = "shapley")
#'
#' # Matrix input
#' C_mat <- matrix(c(0, 12, 15, 12,
#'                  12,  0,  4,  6,
#'                  15,  4,  0,  8,
#'                  12,  6,  8,  0), byrow = TRUE, ncol = 4)
#' mcstGameCC(C_mat, draw = TRUE, which.plot = "main", titles = FALSE)
#'
#' @concept Cooperative Games
#' @concept MCSTP
#'
#' @export

mcstGameCC <- function(C, sol = c("shapley", "nucleolus"), draw = FALSE, which.plot = c("main", "details"), titles = TRUE, method = c("exact", "montecarlo"), nsim = 10000) {

  # Convert input into a standard cost matrix C for N_0 (agents + source)
  C_mat <- .prepare_matrix(C)
  n <- nrow(C_mat) - 1  # Number of agents n = |N|

  sol <- match.arg(sol)
  method <- match.arg(method)
  which.plot <- match.arg(which.plot, several.ok = TRUE)

  # Check if Monte Carlo is actually necessary
  if (sol == "shapley" && method == "montecarlo" && (2^n - 1) <= nsim) {
    message("Warning: (2^n - 1) <= nsim. Consider using method = 'exact' for maximum efficiency")
  }

  # Performance validation
  if (sol == "shapley" && method == "exact" && n > 20) {
    message("Warning: n > 20. Consider using method = 'montecarlo' to reduce processing time")
  }

  if (method == "montecarlo" && sol == "nucleolus") {
    stop("The solution '", sol, "' is only available for method = 'exact'")
  }

  if (sol == "nucleolus" && n > 15) {
    warning("Computing the nucleolus for n > 15 can be extremely slow and memory-intensive")
  }


  ## Execution ##

  # Compute the cycle-complete matrix C*
  C_cc <- .get_cycle_complete(C_mat)

  if (method == "exact") { # Exact

    if (!requireNamespace("CoopGame", quietly = TRUE)) {
      stop("Package 'CoopGame' required for the exact method. Please install it.")
    }

    # Build the characteristic function v^c(S) using binary indexing over C*
    ncoals <- 2^n - 1

    vvals <- vapply(1:ncoals, function(i) {
      # Decode binary index i to find members of coalition S
      S <- which(as.logical(intToBits(i)[1:n]))
      sub_idx <- c(1, S + 1) # Include the source (0)

      # Compute MST cost for this specific subset
      .get_cost(C_cc[sub_idx, sub_idx, drop = FALSE])
    }, numeric(1))

    # Generate all possible coalitions ordered by cardinality for final output
    coalitions <- unlist(lapply(1:n, function(k) combn(n, k, simplify = FALSE)), recursive = FALSE)
    coals_names <- sapply(coalitions, function(S) paste0("{", paste(S, collapse = ","), "}"))
    coals_names[length(coals_names)] <- "N"

    idx <- sapply(coalitions, function(S) sum(2^(S - 1)))
    values <- vvals[idx]
    m_cost <- vvals[ncoals] # Total cost v^c(N)

    # Calculate the allocation based on the chosen solution concept
    if (sol == "shapley") {
      value <- CoopGame::shapleyValue(values)

    } else if (sol == "nucleolus") {
      # Define savings game: v_s(S) = sum(v(i)) - v(S)
      v_s <- sapply(1:ncoals, function(i) {
        S <- which(as.logical(intToBits(i)[1:n]))
        sum(vvals[2^(S - 1)]) - vvals[i]
      })

      # Compute nucleolus of the savings game
      nuc_s <- try(CoopGame::nucleolus(v_s), silent = TRUE)

      if (inherits(nuc_s, "try-error") || is.null(nuc_s)) {
        value <- rep(NA, n)
      } else {
        # Convert savings back to costs: x_i = v(i) - savings_i
        v_ind <- vvals[2^(1:n - 1)]
        value <- v_ind - nuc_s
      }
    }

    names(value) <- as.character(1:n)
    results <- list(value = value, nperms = "Not computed")

  } else { # Monte Carlo (lazy evaluation)

    if (sol == "shapley") {
      # Skip full characteristic function for efficiency
      coals_names <- "Not computed (Lazy Monte Carlo)"
      values <- "Not computed (Lazy Monte Carlo)"
      m_cost <- .get_cost(C_cc)

      # Initialize objects
      perms <- matrix(nrow = nsim, ncol = n)
      contributions <- matrix(0, nrow = nsim, ncol = n, dimnames = list(NULL, as.character(1:n)))

      for (p in 1:nsim) {
        pi <- sample(1:n)
        perms[p, ] <- pi
        predecessors <- c()
        v_old <- 0

        for (i in pi) {
          predecessors <- c(predecessors, i)
          sub_idx <- c(1, predecessors + 1)
          v_new <- .get_cost(C_cc[sub_idx, sub_idx, drop = FALSE])
          contributions[p, i] <- v_new - v_old
          v_old <- v_new
        }
      }

      # Results
      results <- list(
        value = colMeans(contributions),
        contributions = data.frame(pi = apply(perms, 1, paste, collapse = "-"),
                                   contributions, check.names = FALSE),
        nperms = nsim
      )
    }
  }


  ## Visualization ##

  if (draw) {

    show_details <- "details" %in% which.plot
    show_main <- "main" %in% which.plot

    if (n > 1 && n <= 3 && show_details && method == "exact") {

      rows <- if(n == 3) 2 else 1
      cols <- if(n == 3) 3 else 2
      old_par <- par(mfrow = c(rows, cols), oma = c(0, 0, if(titles) 5 else 0, 0))
      on.exit(par(old_par), add = TRUE)

      for (i in 1:(ncoals - 1)) {
        S <- coalitions[[i]]
        sub_idx <- c(1, S + 1)

        C_inf <- matrix(Inf, nrow = n + 1, ncol = n + 1, dimnames = list(0:n, 0:n))

        C_inf[sub_idx, sub_idx] <- C_cc[sub_idx, sub_idx]
        sub_C <- C_cc[sub_idx, sub_idx, drop = FALSE]
        arcs_S <- .get_arcs(sub_C)

        .plot_mcstp(C_inf, arcs_S, NULL,
                    main_title = "", sub_title = paste0("S = {", paste(S, collapse=","), "}"), show_stage = FALSE) #   |  v^c(S): ", round(values[i], 2)
      }

      if (titles) {
        mtext("MCST for each subcoalition S",
              side = 3, line = 1.5, cex = 1.2, font = 2, outer = TRUE)
        mtext("Cycle-complete Game (N, v^c)  |  Method: exact", side = 3, line = 0.1, cex = 0.7, family = "sans", font = 3,
              col = "#444444", outer = TRUE) # paste0("Cycle-complete Game  |  Total Cost v^c(N): ", round(m_cost, 2))
      }

      par(old_par)
    }

    if (n > 1 && n <= 3 && show_details && show_main && method == "exact") {
      devAskNewPage(TRUE)
      on.exit(devAskNewPage(FALSE), add = TRUE)
    }

    if (show_main) {
      arcs_N <- .get_arcs(C_cc)
      tit <- if(titles) "Cycle-complete Game (N, v^c)" else ""
      lab <- if (method == "exact") {"exact"} else {"Monte Carlo"}
      sol_name <- paste0(toupper(substr(sol, 1, 1)), substr(sol, 2, nchar(sol)))
      sub_tit <- if(titles) paste0("Solution: ", sol_name, "  |  Method: ", lab, "  |  Total Cost v^c(N): ", round(m_cost, 2)) else ""
      .plot_mcstp(C_cc, arcs_N, results$value,
                  main_title = tit, sub_title = sub_tit, show_stage = FALSE)
    }
  }

  # Generates a ranking based on allocations, using stars (*) for ties
  ord <- order(-results$value, names(results$value))
  vals <- results$value[ord]; noms <- names(results$value)[ord]
  tab <- table(vals)
  rep_vals <- sort(as.numeric(names(tab[tab > 1])), decreasing = TRUE)

  rank_star <- noquote(sapply(1:length(vals), function(i) {
    if (vals[i] %in% rep_vals) {
      paste0(noms[i], strrep("*", which(rep_vals == vals[i])))
    } else noms[i]
  }))


  ## Output ##

  output <- list(
    coalitions = coals_names,
    C_cc = C_cc,
    v_c = values,
    contributions = if(method == "montecarlo") results$contributions else "Exact method does not use a contributions table",
    allocation = results$value,
    total = m_cost,
    nperms = results$nperms,
    percentage = round((results$value / m_cost) * 100, 2),
    ranking = rank_star,
    method = method,
    sol = sol
  )

  class(output) <- "mcstp_cc"

  return(output)
}

#' @export
print.mcstp_cc <- function(x, ...) {
  if (is.character(x$v_c)) {
    cat("Characteristic function v^c(S) not computed (Monte Carlo efficiency mode)\n")
  } else {
    v_S <- data.frame(
      S = x$coalitions,
      "v^c(S)" = x$v_c,
      row.names = NULL,
      check.names = FALSE
    )
    print(v_S)
  }

  sol_label <- paste0(toupper(substr(x$sol, 1, 1)), substr(x$sol, 2, nchar(x$sol)))
  cat("\nSolution:", sol_label, "\n")
  print(round(x$allocation, 2))
  invisible(x)
}



